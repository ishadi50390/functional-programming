object Main extends App {
  var k, i, j = 2
  var m, n = 5
  var f = 12.0f
  var g = 4.0f
  var c = 'X'

  val a = k + 12 * m       
  val b = m / j            
  val c_mod = n % j            
  val d = m / j * j        
  val e = f + 10 * 5 + g   
  i += 1                   
  val l = i * n            
  println(s"a = $a")
  println(s"b = $b")
  println(s"c_mod = $c")
  println(s"d = $d")
  println(s"e = $e")
  println(s"l = $f")
}
